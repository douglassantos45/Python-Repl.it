import math

angulo = float(input('Digite o ângulo: '))

seno = math.sin(math.radians(angulo))
print('Ângulo {} tem o seno de {}'.format(angulo, seno))

cosseno = math.cos(math.radians(angulo))
print('Ângulo {} tem o cosseno de {}'.format(angulo, cosseno))

tangente = math.tan(math.randians(angulo))
print('Ângulo {} tem o tangente de {}'.format(angulo, tangente))
